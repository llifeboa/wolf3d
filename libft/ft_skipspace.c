#include "libft.h"

char	*ft_skipspace(char const *a)
{
	unsigned int i;

	if (!a)
		return (0);
	i = 0;
	while ((a[i] >= 9 && a[i] <= 13) || a[i] == ' ')
		i++;
	return ((char*)(&a[i]));
}
